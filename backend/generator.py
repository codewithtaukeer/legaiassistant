def _page_footer(canvas, doc):
    canvas.saveState()
    width, _ = A4
    canvas.setStrokeColor(colors.HexColor("#C8C8C8"))
    canvas.setLineWidth(0.6)
    canvas.line(doc.leftMargin, 15 * mm, width - doc.rightMargin, 15 * mm)
    canvas.setFont("Helvetica", 8)
    canvas.setFillColor(colors.HexColor("#666666"))
    canvas.drawString(doc.leftMargin, 10 * mm, "Generated by Legal AI Assistant")
    canvas.drawRightString(width - doc.rightMargin, 10 * mm, f"Page {canvas.getPageNumber()}")
    canvas.restoreState()


def _kv_table(rows: Sequence[Sequence[object]], styles, col_widths=(55 * mm, 115 * mm)):
    data = []
    for key, value in rows:
        data.append([
            Paragraph(f"<b>{_p(key)}</b>", styles["BodyIndian"]),
            Paragraph(_p(value), styles["BodyIndian"]),
        ])
    table = Table(data, colWidths=list(col_widths), hAlign="LEFT")
    table.setStyle(TableStyle([
        ("BACKGROUND",   (0, 0), (0, -1), colors.HexColor("#DCE8F3")),
        ("BACKGROUND",   (1, 0), (1, -1), colors.white),
        ("BOX",          (0, 0), (-1, -1), 0.6, colors.HexColor("#A8C0D8")),
        ("INNERGRID",    (0, 0), (-1, -1), 0.4, colors.HexColor("#A8C0D8")),
        ("VALIGN",       (0, 0), (-1, -1), "TOP"),
        ("TOPPADDING",   (0, 0), (-1, -1), 5),
        ("BOTTOMPADDING",(0, 0), (-1, -1), 5),
        ("LEFTPADDING",  (0, 0), (-1, -1), 6),
        ("RIGHTPADDING", (0, 0), (-1, -1), 6),
    ]))
    return table


def _section(title: str, styles):
    return [
        Paragraph(title.upper(), styles["SectionHeadingIndian"]),
        HRFlowable(width="100%", thickness=0.7, color=colors.HexColor("#2C5F8A"),
                   spaceBefore=0, spaceAfter=4),
    ]


def _clause(number: int, title: str, body: str, styles):
    """Numbered clause with bold title, used in agreements."""
    return [
        Paragraph(f"<b>Clause {number}. {title}</b>", styles["ClauseTitle"]),
        Paragraph(_p(body), styles["BodyJustified"]),
        Spacer(1, 3),
    ]


def _bullets(items: Iterable[str], styles, numbered: bool = True):
    out = []
    for idx, item in enumerate(items, start=1):
        prefix = f"{idx}. " if numbered else "• "
        out.append(Paragraph(_p(prefix + item), styles["BodyIndian"]))
    return out


def _build_pdf(filename: str, story):
    filepath = os.path.join(OUTPUT_DIR, filename)
    doc = SimpleDocTemplate(
        filepath, pagesize=A4,
        leftMargin=18 * mm, rightMargin=18 * mm,
        topMargin=16 * mm, bottomMargin=18 * mm,
        title=filename, author="Legal AI Assistant",
    )
    doc.build(story, onFirstPage=_page_footer, onLaterPages=_page_footer)
    return filename


def _llm_json(prompt: str, mode: str = "auto") -> Dict[str, Any]:
    raw = call_llm(prompt, mode=mode)
    raw = re.sub(r"```json|```", "", raw).strip()
    match = re.search(r"\{.*\}", raw, re.DOTALL)
    if match:
        raw = match.group()
    try:
        return json.loads(raw)
    except Exception:
        def _find(key: str, default: str = "") -> str:
            m = re.search(rf'"{re.escape(key)}"\s*:\s*"(.*?)"', raw, re.DOTALL)
            return m.group(1).replace("\\n", "\n") if m else default
        # Return a best-effort dict with all known keys
        return {k: _find(k) for k in [
            "complainant_name", "complainant_address", "father_name", "address",
            "respondent_name", "respondent_address", "location", "issue",
            "incident_date", "incident_time", "incident_details",
            "police_station", "district", "state", "demand", "reply_days",
            "relationship", "incident_dates", "demand_amount", "additional_details",
            "custom_terms", "landlord_name", "landlord_address", "tenant_name",
            "tenant_address", "house_no", "property_address", "rent_amount",
            "security_deposit", "start_date", "end_date", "notice_period",
            "lock_in_period", "maintenance", "utilities", "name", "age",
            "occupation", "id_proof", "purpose", "custom_facts",
            "smart_reply", "detected_intent",
        ]}


