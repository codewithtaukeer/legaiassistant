def _safe_filename(text: str) -> str:
    text = text or "document"
    text = re.sub(r"[^\w\s.-]", "", str(text), flags=re.UNICODE)
    text = re.sub(r"\s+", "_", text.strip())
    return text[:80] or "document"


def _clean(text: object, default: str = "[Not provided]") -> str:
    if text is None:
        return default
    s = str(text).strip()
    return s if s else default


def _fmt_date(value: Optional[str]) -> str:
    return str(value) if value else "[DD-MM-YYYY]"


def _fmt_time(value: Optional[str]) -> str:
    return str(value) if value else "[HH:MM]"


def _p(value: object) -> str:
    """Escape for ReportLab Paragraph and convert newlines."""
    s = _clean(value)
    s = s.replace("&", "&amp;").replace("<", "&lt;").replace(">", "&gt;")
    s = s.replace("\n", "<br/>")
    return s


