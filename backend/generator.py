def get_risk_level(issue: str) -> str:
    text = (issue or "").lower()
    if any(k in text for k in ["murder", "rape", "kidnap", "kidnapping", "attempt to murder", "sexual assault"]):
        return "🔴 High Risk — Immediate police action required"
    if any(k in text for k in ["fraud", "theft", "stolen", "assault", "hurt", "beaten",
                                "beat", "attacked", "extortion", "harassment", "blackmail"]):
        return "🟡 Medium Risk — Police complaint strongly recommended"
    return "🟢 Low Risk — Formal complaint advised"


def generate_legal_analysis(issue: str, ipc_sections: Optional[Sequence[str]] = None) -> str:
    text = (issue or "").lower()
    ipc_sections = list(ipc_sections or [])
    if any(k in text for k in ["beaten", "beat", "assault", "attacked", "hurt"]):
        return (
            "The facts indicate a physical assault / hurt complaint. "
            "IPC 323 applies to voluntarily causing hurt; IPC 325 applies if the injury is grievous. "
            "IPC 351 covers assault (threat of force). If the accused were in a group, Section 34 (common intention) "
            "or Section 149 (unlawful assembly) may also apply. "
            "Medical examination of the victim should be done immediately to document injuries."
        )
    if any(k in text for k in ["theft", "stolen"]):
        return "The facts indicate a property-offence complaint. IPC 379 applies if the ingredients of dishonest taking of movable property are made out."
    if any(k in text for k in ["fraud", "cheat", "cheating"]):
        return "The facts indicate a cheating/fraud complaint. If dishonest inducement and wrongful loss are shown, IPC 420 applies."
    if "threat" in text or "threaten" in text:
        return "The facts indicate criminal intimidation. If a threat to person, reputation, or property is present, IPC 506 applies."
    if ipc_sections:
        return (
            f"The complaint appears to involve: {', '.join(ipc_sections)}. "
            "Final legal classification depends on the exact facts, witnesses, and supporting evidence."
        )
    return "More details are needed to determine the exact legal sections applicable."


def generate_next_steps(issue: str, police_station: Optional[str] = None) -> List[str]:
    steps = [
        "Carry a printed copy of the complaint along with any photos, videos, medical papers, screenshots, or other proof.",
        f"Submit the complaint at {police_station or 'the nearest police station'} and request an acknowledgement / GD entry number.",
        "Ask for the FIR to be registered if the facts disclose a cognizable offence.",
        "Keep a signed copy or receipt for follow-up.",
        "If the complaint is not accepted, approach the SHO / ACP / SP with the same written complaint and proof of submission.",
    ]
    text = (issue or "").lower()
    if any(k in text for k in ["beaten", "beat", "assault", "attacked", "hurt"]):
        steps.insert(0, "Get a medical examination done IMMEDIATELY and obtain a medico-legal certificate (MLC) from the hospital.")
    return steps


